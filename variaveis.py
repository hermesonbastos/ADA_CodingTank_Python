# > Variáveis

idade = 19 # Criando uma variável

print(idade)

nome = 'Hermeson Bastos'

print(nome)

"""
    Tipos de variáveis

    1. int: números inteiros, ou seja, números sem parte decimal: 0, 5, -1, 1000
    2. float: números reais, ou seja, números com parte decimal
    3. str: cadeias de caracteres, ou seja dados textuais
    4. bool: valores lógicos (booleanos): True ou False
"""

idade = 19
altura = 1.74
nome = 'Hermeson Bastos'
estudando = True

print( type(idade) )
print( type(altura) )
print( type(nome) )
print( type(estudando) )


